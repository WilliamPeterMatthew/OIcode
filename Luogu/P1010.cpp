#include<bits/stdc++.h>
using namespace std;
const int er[20]={1,2,4,8,16,32,64,128,256,512,1024,2048,4096,8192,16384,32768};
const char yi[20][30]={"0","2(0)","2","2+2(0)","2(2)","2(2)+2(0)","2(2)+2","2(2)+2+2(0)","2(2+2(0))","2(2+2(0))+2(0)","2(2+2(0))+2","2(2+2(0))+2+2(0)","2(2+2(0))+2(2)","2(2+2(0))+2(2)+2(0)","2(2+2(0))+2(2)+2","2(2+2(0))+2(2)+2+2(0)","2(2(2))"};
int n,te,i;
int main()
{
    scanf("%d",&n);
    te=n;
    while(te)
    {
        if(te!=n)printf("+");
        i=0;
        while(er[i]<=te){i++;}
        i--;
        if(i==1)printf("2");
        else printf("2(%s)",yi[i]);
        te-=er[i];
    } 
    return 0;
}